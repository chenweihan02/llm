function makeGptLayer(
  index: number,
  label: string,
  attention: number[][],
  cache: KvCacheTrace,
): TraceLayer {
  return {
    index,
    label,
    attentionHead: index === 11 ? 7 : 3,
    heads: 12,
    kvHeads: 12,
    attention,
    qkScale: 0.125,
    hidden: {
      min: -8.42 + index * 0.09,
      max: 9.31 + index * 0.07,
      mean: -0.02 + index * 0.004,
      std: 1.18 + index * 0.03,
      l2: 31.6 + index * 1.7,
    },
    residual: {
      min: -12.2 + index * 0.11,
      max: 13.08 + index * 0.13,
      mean: 0.01 + index * 0.002,
      std: 2.34 + index * 0.04,
      l2: 55.4 + index * 2.1,
    },
    mlp: {
      min: -4.9 + index * 0.04,
      max: 5.72 + index * 0.06,
      mean: 0.03,
      std: 0.78 + index * 0.02,
      l2: 19.2 + index * 1.1,
    },
    kvCache: cache,
    probes: [
      {
        name: "last hidden slice",
        shape: [8],
        sample: [0.18, -0.42, 0.71, 1.03, -0.16, 0.08, -0.91, 0.27],
      },
      {
        name: "attention row for next token",
        shape: [attention.length],
        sample: attention[Math.min(5, attention.length - 1)],
      },
    ],
  };
}

